function SingleCheckIcon({ color = "white", size = 16 }) {
  const bubbleColor = color?.split(" ")[1]?.split("-")[1];

  return (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke={bubbleColor || color}
      strokeWidth="2"
      strokeLinecap="round"
      strokeLinejoin="round"
    >
      <polyline points="20 6 9 17 4 12" />
    </svg>
  );
}

export default SingleCheckIcon;
